      function self = print(self,file)
         if nargin < 2
            file = self.params.output.file;
         end
         
         command = [self.stanHome 'bin/print ' file];
         p = processManager('command',command,...
                            'workingDir',self.workingDir,...
                            'wrap',100,...
                            'keepStdout',false);
         p.block(0.05);
