      function self = stan(varargin)
         [self.defaults,self.validators] = self.stanParams();
         self.params = self.defaults;
         self.modelPath = [pwd filesep];
         self.workingDir = [pwd filesep];
         if nargin == 0
            return;
         end
      
         p = inputParser;
         p.KeepUnmatched= false;
         p.FunctionName = 'stan constructor';
         p.addParamValue('id','',@isstr);
         p.addParamValue('model','',@isstr);
         p.addParamValue('modelPath','',@(x) exist(x,'dir')==7);
         p.addParamValue('workingDir','',@(x) exist(x,'dir')==7);
         p.parse(varargin{:});
         
         self.model = p.Results.model;
         if isempty(p.Results.modelPath)
            self.modelPath = [pwd filesep];
         else
            self.modelPath = p.Results.modelPath;
         end
         if isempty(p.Results.workingDir)
            self.workingDir = [pwd filesep];
         else
            self.workingDir = p.Results.workingDir;
         end
         
         % pass leftover contructor vars to set()
      end
      
